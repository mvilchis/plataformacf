    <h2>Sistema de Monitoreo de Fondeo Colectivo</h2>
    <div class="">
      <p>Es una iniciativa de Crowdfunding México que permite visualizar y descargar información en tiempo real sobre los cuatro tipos principales de fondeo colectivo en México: Capital, Deuda, Donaciones y Recompensa. </p>
      <p>El objetivo principal del Sistema es impulsar el ecosistema de fondeo colectivo a través de un mejor entendimiento de sus actores y las principales variables que los afectan. Un mayor conocimiento del estado del fondeo colectivo en México puede además generar sinergias entre sus jugadores e impulsar un mercado satélite de actores como respuesta a la existencia de un ecosistema creciente.</p>
      <p>A la fecha, la plataforma incluye datos del 80% de los proyectos de fondeo colectivo en México y se está trabajando para que más plataformas se unan todos los días. </p>
    </div>

    <h2>¿Quién es Crowdfunding México?</h2>
    <div class="">
    <p>Es una iniciativa del Fondo Multilateral de Inversiones (FOMIN) del Banco Interamericano de Desarrollo (BID) ejecutado por la Universidad Anáhuac a través del Centro IDEARSE con la participación de Nacional Financiera (NAFIN), el Instituto Nacional del Emprendedor (INADEM), la Oficina de Estrategia Digital de la Presidencia de la República, la Comisión Nacional Bancaria y de Valores (CNBV) y la Asociación de Plataformas de Fondeo Colectivo (AFICO), para impulsar proyectos vía crowdfunding o de financiamiento/fondeo colectivo, ecosistema que está en una etapa inicial en México, y que es considerado un mercado alternativo de fondeo para proyectos innovadores a través de plataformas digitales. </p>
    </div>

    <h2>¿Cómo funciona Crowdfunding México?</h2>
    <div class="">
      <p>El proyecto busca reforzar el ecosistema de financiamiento colectivo mediante la consolidación de la Asociación de Plataformas de Fondeo Colectivo (AFICO), liderada por las principales plataformas de financiamiento colectivo de México. </p>
      <p>El proyecto prevé que se  generen diversos cambios en el comportamiento de los empresarios y las MIPYME, mediante actividades educativas y promocionales, aunadas a la elaboración y diseminación de materiales didácticos pertinentes, fomentarán el uso de las plataformas de financiamiento colectivo entre los empresarios y los microinversionistas.</p>
      <p>Además, a través de la AFICO  se ha buscado recopilar información valiosa de las diversas plataformas de fondeo colectivo en México, con el fin de generar estadísticas confiables y buenas prácticas dentro del ecosistema, que a su vez generen la confianza necesaria para que los usuarios sigan utilizando estos nuevos mecanismos de financiamiento.</p>
